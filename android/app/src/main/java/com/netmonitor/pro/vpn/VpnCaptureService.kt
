    private fun createNotificationChannel() {
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
            val channel = NotificationChannel(
                CHANNEL_ID,
                "NetMonitor 网络监控",
                NotificationManager.IMPORTANCE_LOW
            ).apply {
                description = "NetMonitor Pro VPN 抓包服务运行状态"
                setShowBadge(false)
            }
            val nm = getSystemService(NotificationManager::class.java)
            nm.createNotificationChannel(channel)
        }
    }

    private fun buildNotification(content: String): Notification {
        val stopIntent = Intent(this, VpnCaptureService::class.java).apply {
            action = ACTION_STOP
        }
        val stopPending = PendingIntent.getService(
            this, 0, stopIntent,
            PendingIntent.FLAG_UPDATE_CURRENT or PendingIntent.FLAG_IMMUTABLE
        )

        return NotificationCompat.Builder(this, CHANNEL_ID)
            .setContentTitle("NetMonitor Pro")
            .setContentText(content)
            .setSmallIcon(android.R.drawable.ic_menu_manage)
            .setOngoing(true)
            .addAction(android.R.drawable.ic_media_pause, "停止监控", stopPending)
            .build()
    }

    // ======================================================
    //  公开方法
    // ======================================================

    fun getStats(): Map<String, Long> = mapOf(
        "packets" to packetCount.get(),
        "bytes" to byteCount.get(),
        "tcp" to tcpCount.get(),
        "udp" to udpCount.get(),
        "dns" to dnsCount.get(),
        "tcp_sessions" to tcpSessions.size.toLong(),
        "udp_sessions" to udpSessions.size.toLong(),
    )

    fun drainEvents(): List<NetworkEvent> {
        val events = mutableListOf<NetworkEvent>()
        while (true) {
            val event = eventQueue.poll() ?: break
            queueCount.decrementAndGet()
            events.add(event)
        }
        return events
    }

    fun resetStats() {
        packetCount.set(0)
        byteCount.set(0)
        tcpCount.set(0)
        udpCount.set(0)
        dnsCount.set(0)
    }
